package com.SelfExercise.java;

public class SelfExercise22ToCompareTwoNumbers {
	public static void main(String[] args) {

		int num1 = 22, num2 = 87;

		if (num1 != num2) {
			System.out.println(num1 + "!=" + num2);
		}
		if (num1 == num2) {
			System.out.println(num1 + "=" + num2);
		}
		if (num1 > num2) {
			System.out.println(num1 + ">" + num2);
		}
		if (num1 < num2) {
			System.out.println(num1 + "<" + num2);
		}

	}

}
