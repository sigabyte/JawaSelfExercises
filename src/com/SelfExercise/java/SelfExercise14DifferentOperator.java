package com.SelfExercise.java;

public class SelfExercise14DifferentOperator {

	public static void main(String[] args) {
		int num1=125, num2=24;
		System.out.println(num1+num2);
		System.out.println(num1-num2);
		System.out.println(num1*num2);
		System.out.println(num1/num2);
		System.out.println(num1%num2);
		
	}

}
