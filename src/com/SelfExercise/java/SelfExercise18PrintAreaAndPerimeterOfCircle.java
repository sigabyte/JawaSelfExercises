package com.SelfExercise.java;

public class SelfExercise18PrintAreaAndPerimeterOfCircle {
	public static void main(String[] args) {

		double r = 7.5, permt, area;

		area = 3.14 * (r * r);
		permt = 2 * 3.14 * r;

		System.out.println("Area of the circle =" + area);
		System.out.println("Perimeter of the circle =" + permt);

	}

}
